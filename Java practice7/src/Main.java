public class Main { // Declaration public of class Main
    public static void main(String[] args) {

        System.out.println("This program show the syntax of the 'for' loop");
        int limite = 10; // variable que limitara iteraciones en el ciclo for

        for (int contador = 0; contador < limite; contador++) // The computer counting from 0 to value of variable limite
        {
            System.out.println("Iteration number: " + contador);
        }

    } // End principal metod main
} // End the class Main